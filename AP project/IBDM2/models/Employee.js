import mongoose from "mongoose";

const EmployeeSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
}, { collection: 'Employee' });

const Employee = mongoose.models.Employee || mongoose.model('Employee', EmployeeSchema);

export default Employee;